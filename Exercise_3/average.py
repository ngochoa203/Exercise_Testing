
def average(value, minimum, maximum):
#----------------------------------------------------------------------------------------
    i = 0
    inputNumber = 0                                                             # Node 1
    validNumber = 0
    sum = 0
#----------------------------------------------------------------------------------------
    while i < len(value) and (value[i] != -999) and (inputNumber < 100):        # Node 2
#----------------------------------------------------------------------------------------
        inputNumber += 1                                                        # Node 3
#----------------------------------------------------------------------------------------
        if (value[i] >= minimum) and (value[i] <= maximum):                     # Node 4
#----------------------------------------------------------------------------------------
            validNumber += 1                                                    # Node 5
            sum += value[i]
#----------------------------------------------------------------------------------------
        i += 1                                                                  # Node 6
#----------------------------------------------------------------------------------------
    if validNumber > 0:                                                         # Node 7
#----------------------------------------------------------------------------------------
        return sum / validNumber                                                # Node 8
#----------------------------------------------------------------------------------------
    else:
#----------------------------------------------------------------------------------------
        return -999                                                             # Node 9
#----------------------------------------------------------------------------------------





